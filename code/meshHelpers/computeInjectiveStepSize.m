        x = xy(:,1); x = x(F);
        y = xy(:,2); y = y(F);
        p = pq(:,1); p = p(F);
        q = pq(:,2); q = q(F);
        
        a = p(:,1).*q(:,2) - p(:,2).*q(:,1) - p(:,1).*q(:,3) + p(:,3).*q(:,1) + p(:,2).*q(:,3) - p(:,3).*q(:,2);
