        ff = (abs(a)<=tol);
        ti1(ff) = -c(ff)./b(ff);
        ti2(ff) = ti1(ff);
        % deal with pseudo complex roots
        ff=(abs(imag(ti1))<=tol);
        ti1(ff) = real(ti1(ff));
        %
        ff=(abs(imag(ti2))<=tol);
        ti2(ff) = real(ti2(ff));
        % get rid of negatives/imaginaries
        ti1(ti1<0 | imag(ti1)~=0) = inf;
        ti2(ti2<0 | imag(ti2)~=0) = inf;
        % take minimal value
        tmax = min(min(ti1),min(ti2));
        
